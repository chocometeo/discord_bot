package discord_bot.common;

/*
 *  チャットメッセージ
 */
public class ChatMessage {
	 public String userName;
	 public String message;
	 public boolean result;

	 public String getUserName() {
		 return userName;
	 }
	 public String getMessage() {
		 return message;
	 }
	 public boolean getResult() {
		 return result;
	 }
}
